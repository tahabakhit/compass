/**
 * COMPAT-07: Agent definition translation
 * Validates that agents/*.md translate to valid .codex/agents/*.toml.
 */

'use strict';

const fs   = require('fs');
const path = require('path');
const os   = require('os');
const { execSync } = require('child_process');

async function run() {
  const errors = [];
  const tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), 'citadel-agent-test-'));

  try {
    fs.mkdirSync(path.join(tmpDir, '.claude'), { recursive: true });

    // Run codex-compat.js
    const script = path.join(__dirname, '..', 'codex-compat.js');
    execSync(`node "${script}" "${tmpDir}"`, {
      encoding: 'utf8',
      timeout: 15000,
      stdio: ['pipe', 'pipe', 'pipe'],
    });

    const agentsDir = path.join(tmpDir, '.codex', 'agents');
    if (!fs.existsSync(agentsDir)) {
      return { pass: false, message: '.codex/agents/ directory not created' };
    }

    const tomlFiles = fs.readdirSync(agentsDir).filter(f => f.endsWith('.toml'));
    if (tomlFiles.length === 0) {
      return { pass: false, message: 'No agent TOML files generated' };
    }

    // Expected agents from Sinan source
    const expectedAgents = ['archon', 'fleet', 'arch-reviewer', 'knowledge-extractor', 'phase-validator', 'policy-enforcer'];

    for (const expected of expectedAgents) {
      const tomlPath = path.join(agentsDir, `${expected}.toml`);
      if (!fs.existsSync(tomlPath)) {
        errors.push(`Missing: ${expected}.toml`);
        continue;
      }

      const content = fs.readFileSync(tomlPath, 'utf8');

      // Required fields
      if (!content.includes('name = ')) {
        errors.push(`${expected}.toml: missing name`);
      }
      if (!content.includes('description = ')) {
        errors.push(`${expected}.toml: missing description`);
      }
      if (!content.includes('developer_instructions')) {
        errors.push(`${expected}.toml: missing developer_instructions`);
      }
      if (!content.includes('model = ')) {
        errors.push(`${expected}.toml: missing model`);
      }

      // Model should be a valid Codex model
      const modelMatch = content.match(/model = "([^"]+)"/);
      if (modelMatch) {
        const model = modelMatch[1];
        if (!model.startsWith('gpt-')) {
          errors.push(`${expected}.toml: model "${model}" should be a gpt-* model`);
        }
      }

      // Should have generation header
      if (!content.includes('Generated by Sinan')) {
        errors.push(`${expected}.toml: missing generation header`);
      }
    }

  } finally {
    fs.rmSync(tmpDir, { recursive: true, force: true });
  }

  if (errors.length > 0) {
    return { pass: false, message: errors.join('; ') };
  }
  return { pass: true, message: `All ${expectedAgents.length} agent definitions translated correctly` };
}

// For the return message outside the try block
const expectedAgents = ['archon', 'fleet', 'arch-reviewer', 'knowledge-extractor', 'phase-validator'];

module.exports = { run };
