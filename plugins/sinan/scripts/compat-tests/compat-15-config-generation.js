/**
 * COMPAT-15: Config generation
 * Validates that codex-compat.js produces valid .codex/config.toml.
 */

'use strict';

const fs   = require('fs');
const path = require('path');
const os   = require('os');
const { execSync } = require('child_process');

async function run() {
  const errors = [];
  const tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), 'sinan-compat-test-'));

  try {
    // Create a minimal project structure
    fs.mkdirSync(path.join(tmpDir, '.claude'), { recursive: true });

    // Run codex-compat.js
    const script = path.join(__dirname, '..', 'codex-compat.js');
    execSync(`node "${script}" "${tmpDir}"`, {
      encoding: 'utf8',
      timeout: 15000,
      stdio: ['pipe', 'pipe', 'pipe'],
    });

    // Check .codex/config.toml exists
    const configPath = path.join(tmpDir, '.codex', 'config.toml');
    if (!fs.existsSync(configPath)) {
      errors.push('config.toml was not generated');
    } else {
      const content = fs.readFileSync(configPath, 'utf8');

      // Validate required feature flags
      if (!content.includes('hooks = true')) {
        errors.push('Missing: hooks = true');
      }
      if (content.includes('codex_hooks = true')) {
        errors.push('Deprecated feature key still present: codex_hooks = true');
      }
      if (!content.includes('multi_agent = true')) {
        errors.push('Missing: multi_agent = true');
      }
      if (!content.includes('skill_mcp_dependency_install = true')) {
        errors.push('Missing: skill_mcp_dependency_install');
      }
      if (!content.includes('max_depth = 1')) {
        errors.push('Missing: max_depth = 1');
      }
      if (!content.includes('max_threads = 6')) {
        errors.push('Missing: max_threads = 6');
      }
      if (!content.includes('persistence = "save-all"')) {
        errors.push('Missing: persistence = "save-all"');
      }
      if (!content.includes('SINAN_RUNTIME = "codex"')) {
        errors.push('Missing: SINAN_RUNTIME env var');
      }
      if (!content.includes('Generated by Sinan')) {
        errors.push('Missing: generation header comment');
      }
    }

    // Check .codex-plugin/plugin.json exists
    const pluginPath = path.join(tmpDir, '.codex-plugin', 'plugin.json');
    if (!fs.existsSync(pluginPath)) {
      errors.push('plugin.json was not generated');
    } else {
      const raw = fs.readFileSync(pluginPath, 'utf8');
      // Strip the comment line before parsing
      const jsonStr = raw.replace(/^\/\/.*\n/, '');
      try {
        const manifest = JSON.parse(jsonStr);
        if (!manifest.name) errors.push('plugin.json missing name');
        if (!manifest.version) errors.push('plugin.json missing version');
        if (!manifest.description) errors.push('plugin.json missing description');
        if (!manifest.skills) errors.push('plugin.json missing skills pointer');
        if (manifest.hooks) errors.push('plugin.json should rely on default hooks/hooks.json');
        if (!manifest.interface) errors.push('plugin.json missing interface');
        if (/claude/i.test(manifest.description)) errors.push('plugin.json description should be Codex-native, not Claude-specific');
        if (!/(Codex-native|Sinan orchestration)/.test(manifest.interface.shortDescription || '')) errors.push('plugin.json shortDescription should describe orchestration');
        if (!manifest.interface.websiteURL) errors.push('plugin.json interface missing websiteURL');
        if (!manifest.interface.privacyPolicyURL) errors.push('plugin.json interface missing privacyPolicyURL');
        if (!manifest.interface.termsOfServiceURL) errors.push('plugin.json interface missing termsOfServiceURL');
      } catch (e) {
        errors.push(`plugin.json is invalid JSON: ${e.message}`);
      }
    }

    const pluginHooksPath = path.join(tmpDir, 'hooks', 'hooks.json');
    if (!fs.existsSync(pluginHooksPath)) {
      errors.push('plugin-bundled hooks/hooks.json was not generated');
    } else {
      const pluginHooks = JSON.parse(fs.readFileSync(pluginHooksPath, 'utf8'));
      if (!pluginHooks.hooks || !pluginHooks.hooks.PermissionRequest) {
        errors.push('plugin hooks missing PermissionRequest');
      }
      const firstHook = pluginHooks.hooks.PermissionRequest?.[0]?.hooks?.[0];
      if (!firstHook?.command?.includes('${PLUGIN_ROOT}')) {
        errors.push('plugin hooks should use PLUGIN_ROOT-relative commands');
      }
      if (!firstHook?.command?.includes('${CLAUDE_PLUGIN_ROOT')) {
        errors.push('plugin hooks should fall back to CLAUDE_PLUGIN_ROOT');
      }
      if (!firstHook?.commandWindows?.includes('%PLUGIN_ROOT%')) {
        errors.push('plugin hooks should include Windows PLUGIN_ROOT command');
      }
    }

    // Check agent TOML files were generated
    const agentsDir = path.join(tmpDir, '.codex', 'agents');
    if (fs.existsSync(agentsDir)) {
      const tomlFiles = fs.readdirSync(agentsDir).filter(f => f.endsWith('.toml'));
      if (tomlFiles.length === 0) {
        errors.push('No agent TOML files generated');
      } else {
        // Validate one TOML file has required fields
        const sample = fs.readFileSync(path.join(agentsDir, tomlFiles[0]), 'utf8');
        if (!sample.includes('name = ')) errors.push('Agent TOML missing name');
        if (!sample.includes('description = ')) errors.push('Agent TOML missing description');
        if (!sample.includes('developer_instructions')) errors.push('Agent TOML missing instructions');
      }
    }

    // Check skills were synced
    const skillsDir = path.join(tmpDir, '.agents', 'skills');
    if (!fs.existsSync(skillsDir)) {
      errors.push('.agents/skills/ directory was not created');
    } else {
      const skillDirs = fs.readdirSync(skillsDir);
      if (skillDirs.length === 0) {
        errors.push('No skills were synced');
      } else {
        // Check one skill has SKILL.md and openai.yaml
        const sampleSkill = skillDirs[0];
        const skillMd = path.join(skillsDir, sampleSkill, 'SKILL.md');
        const openaiYaml = path.join(skillsDir, sampleSkill, 'agents', 'openai.yaml');
        if (!fs.existsSync(skillMd)) errors.push(`${sampleSkill}/SKILL.md missing`);
        if (!fs.existsSync(openaiYaml)) errors.push(`${sampleSkill}/agents/openai.yaml missing`);
      }
    }

  } finally {
    // Cleanup
    fs.rmSync(tmpDir, { recursive: true, force: true });
  }

  if (errors.length > 0) {
    return { pass: false, message: errors.join('; ') };
  }
  return { pass: true, message: 'Config generation produces valid artifacts' };
}

module.exports = { run };
