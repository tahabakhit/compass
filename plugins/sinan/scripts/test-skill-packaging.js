#!/usr/bin/env node

'use strict';

const assert = require('assert');
const { execFileSync } = require('child_process');
const fs = require('fs');
const os = require('os');
const path = require('path');

const { buildSkillCatalog, validateOptionalMetadata } = require('../core/skills/catalog');
const { scaffold } = require('./skill-scaffold');

function withTempProject(run) {
  const dir = fs.mkdtempSync(path.join(os.tmpdir(), 'citadel-skill-packaging-'));
  try {
    run(dir);
  } finally {
    fs.rmSync(dir, { recursive: true, force: true });
  }
}

withTempProject((projectRoot) => {
  const result = scaffold({
    projectRoot,
    name: 'sample-flow',
    description: 'Sample workflow generated by the scaffold test.',
    taskClass: 'quality',
    riskLevel: 'low',
    withBenchmark: true,
    write: true,
  });
  assert.equal(result.ok, true);
  assert(fs.existsSync(path.join(projectRoot, 'skills', 'sample-flow', 'SKILL.md')));
  assert(fs.existsSync(path.join(projectRoot, 'skills', 'sample-flow', '__benchmarks__', 'basic.md')));

  const catalog = buildSkillCatalog(projectRoot);
  assert.equal(catalog.skillCount, 1);
  assert.equal(catalog.skills[0].taskClass, 'quality');
  assert.equal(catalog.skills[0].riskLevel, 'low');
  assert.equal(catalog.skills[0].benchmarkCount, 1);

  assert.deepEqual(validateOptionalMetadata({ 'task-class': 'quality', 'risk-level': 'low' }), []);
  assert(validateOptionalMetadata({ 'task-class': 'bogus' })[0].includes('task-class'));

  const cli = execFileSync(process.execPath, [
    path.join(__dirname, 'skill-catalog.js'),
    '--project-root',
    projectRoot,
    '--task-class',
    'quality',
    '--json',
  ], { encoding: 'utf8' });
  assert.equal(JSON.parse(cli).skills.length, 1);
});

console.log('skill packaging tests passed');
