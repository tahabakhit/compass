#!/usr/bin/env node
"use strict";

const fs = require("node:fs");
const path = require("node:path");

const MARKER_START = "<!-- GENERATED BY SINAN: scaffold v1 START -->";
const MARKER_END = "<!-- GENERATED BY SINAN: scaffold v1 END -->";
const AGENT_SCAFFOLD_MARKER_START = "<!-- GENERATED BY SINAN: agent-scaffold v1 START -->";
const AGENT_SCAFFOLD_MARKER_END = "<!-- GENERATED BY SINAN: agent-scaffold v1 END -->";
const GLOSSARY_MARKER_START = "<!-- GENERATED BY SINAN: glossary-conventions v1 START -->";
const GLOSSARY_MARKER_END = "<!-- GENERATED BY SINAN: glossary-conventions v1 END -->";
const ADR_MARKER_START = "<!-- GENERATED BY SINAN: adr-conventions v1 START -->";
const ADR_MARKER_END = "<!-- GENERATED BY SINAN: adr-conventions v1 END -->";
const PLANNING_MARKER_START = "<!-- GENERATED BY SINAN: planning-conventions v1 START -->";
const PLANNING_MARKER_END = "<!-- GENERATED BY SINAN: planning-conventions v1 END -->";
const WORKFLOW_STATE_MARKER_START = "<!-- GENERATED BY SINAN: workflow-state-conventions v1 START -->";
const WORKFLOW_STATE_MARKER_END = "<!-- GENERATED BY SINAN: workflow-state-conventions v1 END -->";
const LABEL_MARKER_START = "# GENERATED BY SINAN: github-labels v1 START";
const LABEL_MARKER_END = "# GENERATED BY SINAN: github-labels v1 END";
const WORKFLOW_MARKER_START = "# GENERATED BY SINAN: agent-checks-workflow v1 START";
const WORKFLOW_MARKER_END = "# GENERATED BY SINAN: agent-checks-workflow v1 END";

function block(body) {
  return `${MARKER_START}\n${body.trim()}\n${MARKER_END}\n`;
}

function markedBlock(start, end, body) {
  return `${start}\n${body.trim()}\n${end}\n`;
}

function agentsBlock() {
  return block(`
## Sinan Startup Workflow

Use Sinan as the front door for ambiguous or multi-step work:

1. Use \`$bootstrap\` as the front door for new or uncertain repos; run \`node scripts/bootstrap-report.js --target <repo> --json\` when available, and inspect any pasted handoff, \`.planning/handoffs/\`, \`.workflow-state/runs/\`, or continuation notes before proposing startup work.
2. Shape the idea with \`$brainstorm\` until users, constraints, workflows, and the first vertical slice are clear.
3. Capture durable terms in \`GLOSSARY.md\` and durable architecture decisions in \`docs/adr/\` only after confirmation.
4. Use \`$architecture\` before implementation to choose boundaries, modules, data shape, integrations, and the first vertical slice.
5. Run \`$scaffold\` to create or refresh agent instructions after the repo direction is known.
6. Use \`$starter\` for generating initial framework/app files after product and technical decisions exist.
7. Plan the first slice with \`$task-router\`, \`$architecture\`, or the \`implement\` workflow as appropriate.
8. Implement with \`$tdd\`, review with \`$review\`, deepen architecture with \`$architecture-deepening\`, and end resumable sessions with \`$handoff\`.

For tiny command, read, rewrite, or translation tasks, answer directly without loading workflows, agents, or extra skills.

## Project Memory

- Use \`.planning/\` for human-readable working memory, campaigns, plans, reviews, and handoffs.
- Use \`.planning/handoffs/\` for canonical continuation notes.
- Use \`.workflow-state/\` only for generated machine-readable workflow state.
- Keep \`GLOSSARY.md\` as domain vocabulary only.
- Keep ADRs under \`docs/adr/\` for durable architecture decisions.
- Use repo \`.wiki/\` for project knowledge and \`~/.wiki/\` for personal/global knowledge; route wiki writes through Zhi when available.
- Preserve manual text outside generated Sinan blocks.

## GitHub Conventions

- Prefer labels: \`needs-triage\`, \`needs-info\`, \`ready-for-agent\`, \`ready-for-human\`, \`bug\`, \`enhancement\`, \`docs\`, and \`architecture\`.
- Inspect existing labels and \`.github/workflows/\` before proposing changes.
- Create or edit workflow templates only after confirming the repo's package manager, test command, branch policy, and deployment target.
- Keep issue triage separate from architecture decisions; link ADRs from issues or PRs when a decision becomes durable.
`);
}

