#!/usr/bin/env node
"use strict";

const fs = require("node:fs");
const path = require("node:path");

const MARKER_START = "<!-- GENERATED BY SINAN: project-scaffold v1 START -->";
const MARKER_END = "<!-- GENERATED BY SINAN: project-scaffold v1 END -->";

function block(body) {
  return `${MARKER_START}\n${body.trim()}\n${MARKER_END}\n`;
}

function agentsBlock() {
  return block(`
## Sinan Greenfield Workflow

Use Sinan as the front door for ambiguous or multi-step work:

1. Shape the idea with \`$grill\` until users, constraints, workflows, and the first vertical slice are clear.
2. Capture durable terms in \`CONTEXT.md\` and durable architecture decisions in \`docs/adr/\` only after confirmation.
3. Plan the first slice with \`$task-router\`, \`$architecture\`, or \`$project-scaffold\` as appropriate.
4. Implement with the \`implement\` workflow and \`$tdd\`.
5. Review with \`$review\`.
6. Use \`$architecture-deepening\` when shallow modules, weak interfaces, or testability friction appear.
7. End sessions with \`$handoff\` when work must be resumed.

For tiny command, read, rewrite, or translation tasks, answer directly without loading workflows, agents, or extra skills.

## Project Memory

- Keep \`CONTEXT.md\` as domain vocabulary only.
- Keep ADRs under \`docs/adr/\` for durable architecture decisions.
- Preserve manual text outside generated Sinan blocks.
`);
}

function claudeBlock() {
  return block(`
## Claude Code Notes

- Use \`@AGENTS.md\` as the canonical shared project guidance.
- Use Claude plan mode for risky multi-file changes, cleanup, and architecture work.
- Prefer Claude-native tools when they fit, but follow the same Sinan workflow names and gates.
- Keep Claude-specific tool notes here instead of duplicating shared project context.
`);
}

function upsertGeneratedBlock(existing, generatedBlock) {
  if (!existing.trim()) return `${generatedBlock}`;
  const start = existing.indexOf(MARKER_START);
  const end = existing.indexOf(MARKER_END);
  if (start !== -1 && end !== -1 && end > start) {
    const afterEnd = end + MARKER_END.length;
    return `${existing.slice(0, start)}${generatedBlock.trimEnd()}${existing.slice(afterEnd)}`
      .replace(/\n{3,}/g, "\n\n")
      .trimEnd() + "\n";
  }
  return `${existing.trimEnd()}\n\n${generatedBlock}`;
}

function ensureClaudeImport(existing) {
  const trimmed = existing.trimStart();
  if (trimmed.startsWith("@AGENTS.md")) return existing;
  return `@AGENTS.md\n\n${existing.trimStart()}`;
}

function readIfExists(filePath) {
  return fs.existsSync(filePath) ? fs.readFileSync(filePath, "utf8") : "";
}

function desiredFiles(target) {
  const agentsPath = path.join(target, "AGENTS.md");
  const claudePath = path.join(target, "CLAUDE.md");
  const agents = upsertGeneratedBlock(readIfExists(agentsPath), agentsBlock());
  const claude = upsertGeneratedBlock(ensureClaudeImport(readIfExists(claudePath)), claudeBlock());
  return [
    { path: agentsPath, content: agents },
    { path: claudePath, content: claude },
  ];
}

function parseArgs(argv) {
  const args = { target: process.cwd(), check: false, json: false };
  for (let index = 2; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--target") args.target = argv[++index];
    else if (arg === "--check") args.check = true;
    else if (arg === "--json") args.json = true;
    else if (arg === "--help") {
      printHelp();
      process.exit(0);
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }
  return args;
}

function printHelp() {
  console.log(`Usage: node scripts/scaffold-instructions.js [--target <repo>] [--check] [--json]

Creates or verifies AGENTS.md and CLAUDE.md instruction scaffolding.
`);
}

function run(args) {
  const target = path.resolve(args.target);
  if (!fs.existsSync(target) || !fs.statSync(target).isDirectory()) {
    throw new Error(`target must be a directory: ${target}`);
  }

  const files = desiredFiles(target);
  const changes = [];
  for (const file of files) {
    const current = readIfExists(file.path);
    if (current !== file.content) {
      changes.push(path.relative(target, file.path));
      if (!args.check) {
        fs.writeFileSync(file.path, file.content, "utf8");
      }
    }
  }

  return {
    target,
    mode: args.check ? "check" : "write",
    changed: changes,
    ok: changes.length === 0 || !args.check,
  };
}

function main() {
  const args = parseArgs(process.argv);
  const result = run(args);
  if (args.check && result.changed.length > 0) {
    if (args.json) process.stdout.write(`${JSON.stringify(result, null, 2)}\n`);
    else console.error(`instruction scaffold is stale: ${result.changed.join(", ")}`);
    process.exit(1);
  }
  if (args.json) process.stdout.write(`${JSON.stringify(result, null, 2)}\n`);
  else {
    const action = args.check ? "instruction scaffold check passed" : "instruction scaffold written";
    console.log(action);
    if (result.changed.length > 0) console.log(`changed: ${result.changed.join(", ")}`);
  }
}

if (require.main === module) {
  try {
    main();
  } catch (error) {
    console.error(error.message);
    process.exit(1);
  }
}

module.exports = {
  MARKER_START,
  MARKER_END,
  agentsBlock,
  claudeBlock,
  desiredFiles,
  run,
  upsertGeneratedBlock,
};
