function upsertMarkedBlock(existing, generatedBlock, markerPairs) {
  if (!existing.trim()) return `${generatedBlock}`;
  const markerPair = markerPairs.find(([start]) => existing.includes(start)) || markerPairs[markerPairs.length - 1];
  const [markerStart, markerEnd] = markerPair;
  const start = existing.indexOf(markerStart);
  const end = existing.indexOf(markerEnd);
  if (start !== -1 && end !== -1 && end > start) {
    const afterEnd = end + markerEnd.length;
    return `${existing.slice(0, start)}${generatedBlock.trimEnd()}${existing.slice(afterEnd)}`
      .replace(/\n{3,}/g, "\n\n")
      .trimEnd() + "\n";
  }
  return `${existing.trimEnd()}\n\n${generatedBlock}`;
}

function upsertGeneratedBlock(existing, generatedBlock) {
  return upsertMarkedBlock(existing, generatedBlock, [
    [MARKER_START, MARKER_END],
    [AGENT_SCAFFOLD_MARKER_START, AGENT_SCAFFOLD_MARKER_END],
  ]);
}

function upsertGlossaryBlock(existing) {
  const base = existing.trim() ? existing : "# Glossary\n";
  return upsertMarkedBlock(base, glossaryBlock(), [[GLOSSARY_MARKER_START, GLOSSARY_MARKER_END]]);
}

function upsertAdrReadmeBlock(existing) {
  const base = existing.trim() ? existing : "# Architecture Decisions\n";
  return upsertMarkedBlock(base, adrReadmeBlock(), [[ADR_MARKER_START, ADR_MARKER_END]]);
}

function generatedWholeFile(existing, generatedBlock, markerStart, markerEnd) {
  if (!existing.trim()) return { content: generatedBlock, skipped: false };
  if (!existing.includes(markerStart) || !existing.includes(markerEnd)) {
    return { content: existing, skipped: true };
  }
  return {
    content: upsertMarkedBlock(existing, generatedBlock, [[markerStart, markerEnd]]),
    skipped: false,
  };
}

function ensureClaudeImport(existing) {
  const trimmed = existing.trimStart();
  if (trimmed.startsWith("@AGENTS.md")) return existing;
  return `@AGENTS.md\n\n${existing.trimStart()}`;
}

function readIfExists(filePath) {
  return fs.existsSync(filePath) ? fs.readFileSync(filePath, "utf8") : "";
}

function desiredFiles(target) {
  const agentsPath = path.join(target, "AGENTS.md");
  const claudePath = path.join(target, "CLAUDE.md");
  const glossaryPath = path.join(target, "GLOSSARY.md");
  const adrReadmePath = path.join(target, "docs", "adr", "README.md");
  const labelsPath = path.join(target, ".github", "labels.yml");
  const workflowPath = path.join(target, ".github", "workflows", "agent-checks.yml");
  const planningReadmePath = path.join(target, ".planning", "README.md");
  const workflowStateReadmePath = path.join(target, ".workflow-state", "README.md");
  const labels = generatedWholeFile(readIfExists(labelsPath), labelsFile(), LABEL_MARKER_START, LABEL_MARKER_END);
  const workflow = generatedWholeFile(readIfExists(workflowPath), workflowFile(), WORKFLOW_MARKER_START, WORKFLOW_MARKER_END);
  const agents = upsertGeneratedBlock(readIfExists(agentsPath), agentsBlock());
  const claude = upsertGeneratedBlock(ensureClaudeImport(readIfExists(claudePath)), claudeBlock());
  const templates = planningTemplates();
  const planningTemplateFiles = Object.entries(templates).map(([name, content]) => {
    const filePath = path.join(target, ".planning", "_templates", name);
    const generated = generatedWholeFile(readIfExists(filePath), content, PLANNING_MARKER_START, PLANNING_MARKER_END);
    return { path: filePath, content: generated.content, skipped: generated.skipped };
  });
  const planningDirs = [
    ".planning/inbox",
    ".planning/brainstorm",
    ".planning/research",
    ".planning/campaigns/active",
    ".planning/campaigns/paused",
    ".planning/campaigns/completed",
    ".planning/plans/proposed",
    ".planning/plans/accepted",
    ".planning/plans/superseded",
    ".planning/reviews",
    ".planning/handoffs",
    ".planning/archive",
    ".workflow-state/plans",
    ".workflow-state/runs",
  ].map((relativePath) => ({
    path: path.join(target, relativePath, ".gitkeep"),
    content: "",
    skipped: false,
    createIfMissing: true,
  }));
  return [
    { path: agentsPath, content: agents, skipped: false },
    { path: claudePath, content: claude, skipped: false },
    { path: glossaryPath, content: upsertGlossaryBlock(readIfExists(glossaryPath)), skipped: false },
    { path: adrReadmePath, content: upsertAdrReadmeBlock(readIfExists(adrReadmePath)), skipped: false },
    {
      path: planningReadmePath,
      ...generatedWholeFile(readIfExists(planningReadmePath), planningReadmeFile(), PLANNING_MARKER_START, PLANNING_MARKER_END),
    },
    {
      path: workflowStateReadmePath,
      ...generatedWholeFile(
        readIfExists(workflowStateReadmePath),
        workflowStateReadmeFile(),
        WORKFLOW_STATE_MARKER_START,
        WORKFLOW_STATE_MARKER_END,
      ),
    },
    ...planningTemplateFiles,
    ...planningDirs,
    { path: labelsPath, content: labels.content, skipped: labels.skipped },
    { path: workflowPath, content: workflow.content, skipped: workflow.skipped },
  ];
}

function parseArgs(argv) {
  const args = { target: process.cwd(), check: false, json: false };
  for (let index = 2; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--target") args.target = argv[++index];
    else if (arg === "--check") args.check = true;
    else if (arg === "--json") args.json = true;
    else if (arg === "--help") {
      printHelp();
      process.exit(0);
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }
  return args;
}

function printHelp() {
  console.log(`Usage: node scripts/scaffold-instructions.js [--target <repo>] [--check] [--json]

Creates or verifies agent guidance, memory conventions, labels, and agent-check workflow scaffolding.
`);
}

function run(args) {
  const target = path.resolve(args.target);
  if (!fs.existsSync(target) || !fs.statSync(target).isDirectory()) {
    throw new Error(`target must be a directory: ${target}`);
  }

  const files = desiredFiles(target);
  const changes = [];
  const skipped = [];
  for (const file of files) {
    const relativePath = path.relative(target, file.path);
    if (file.skipped) {
      skipped.push({ path: relativePath, reason: "existing file has no Sinan marker" });
      continue;
    }
    if (file.createIfMissing && fs.existsSync(file.path)) continue;
    const current = readIfExists(file.path);
    if ((file.createIfMissing && !fs.existsSync(file.path)) || current !== file.content) {
      changes.push(relativePath);
      if (!args.check) {
        fs.mkdirSync(path.dirname(file.path), { recursive: true });
        fs.writeFileSync(file.path, file.content, "utf8");
      }
    }
  }

  return {
    target,
    mode: args.check ? "check" : "write",
    changed: changes,
    skipped,
    ok: changes.length === 0 || !args.check,
  };
}

function main() {
  const args = parseArgs(process.argv);
  const result = run(args);
  if (args.check && result.changed.length > 0) {
    if (args.json) process.stdout.write(`${JSON.stringify(result, null, 2)}\n`);
    else console.error(`instruction scaffold is stale: ${result.changed.join(", ")}`);
    process.exit(1);
  }
  if (args.json) process.stdout.write(`${JSON.stringify(result, null, 2)}\n`);
  else {
    const action = args.check ? "instruction scaffold check passed" : "instruction scaffold written";
    console.log(action);
    if (result.changed.length > 0) console.log(`changed: ${result.changed.join(", ")}`);
  }
}

if (require.main === module) {
  try {
    main();
  } catch (error) {
    console.error(error.message);
    process.exit(1);
  }
}

